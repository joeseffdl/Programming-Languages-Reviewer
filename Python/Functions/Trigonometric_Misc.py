import math

# degrees() function converts angle x from radians to degrees.
print("degrees(3.141592653589793) : ", math.degrees(3.141592653589793))
print("degrees(-3.141592653589793) : ", math.degrees(-3.141592653589793))

# radians() function converts angle x from degrees to radians.
print("radians(180.0) : ", math.radians(180.0))
print("radians(-180.0) : ", math.radians(-180.0))
print("radians(360.0) : ", math.radians(360.0))

# hypot() function returns the Euclidean norm, sqrt(x*x + y*y).
print("hypot(3, 2) : ", math.hypot(3, 2))

# pi() function returns the value of PI.
print("pi : ", math.pi)

# e() function returns the value of e.
print("e : ", math.e)

