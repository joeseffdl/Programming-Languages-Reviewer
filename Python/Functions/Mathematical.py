import math

# abs() function returns the absolute value of a number.
print("abs(-45) : ", abs(-45))
print("abs(100.12) : ", abs(100.12))

# ceil() function 
print("ceil(-45.17) : ", math.ceil(-45.17))
print("ceil(100.12) : ", math.ceil(100.12))

# exp() function returns exponential of x: e^x
print("exp(-45.17) : ", math.exp(-45.17))
print("exp(100.12) : ", math.exp(100.12))

# floor() function 
print("floor(-45.17) : ", math.floor(-45.17))
print("floor(100.12) : ", math.floor(100.12))

# log() function returns the natural logarithm of x, for x > 0.
print("log(100.12) : ", math.log(100.12))
print("log(100.72) : ", math.log(100.72))
print("log(119) : ", math.log(119))

# log10() function returns the base-10 logarithm of x for x > 0.
print("log10(100.12) : ", math.log10(100.12))
print("log10(100.72) : ", math.log10(100.72))
print("log10(119) : ", math.log10(119))

# max() function returns the largest of its arguments: the value closest to positive infinity.
print("max(80, 100, 1000) : ", max(80, 100, 1000))
print("max(-20, 100, 400) : ", max(-20, 100, 400))

# min() function returns the smallest of its arguments: the value closest to negative infinity.
print("min(80, 100, 1000) : ", min(80, 100, 1000))
print("min(-20, 100, 400) : ", min(-20, 100, 400))

# pow() function returns the value of x to the power of y (xy).
print("pow(100, 2) : ", pow(100, 2))
print("pow(100, -2) : ", pow(100, -2))

# round() function returns the floating point number rounded off to the given ndigits digits after the decimal point. If no ndigits is provided, it rounds off the number to the nearest integer.
print("round(80.23456, 2) : ", round(80.23456, 2))
print("round(100.000056, 3) : ", round(100.000056, 3))

# sqrt() function returns the square root of x for x > 0.
print("sqrt(100) : ", math.sqrt(100))


