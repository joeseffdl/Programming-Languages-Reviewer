string = "Hello World!"

print("Prints complete string using 'print(string)': ", string)
print("Prints first character of the string using 'print(string[0])': ", string[0])
print("Prints characters starting from 3rd to 5th using 'print(string[2:5])': ", string[2:5])
print("Prints string starting from 3rd character using 'print(string[2:])': ", string[2:])
print("Prints string two times using 'print(string * 2)': ", string * 2)
print("Prints concatenated string using 'print(string + \"TEST\")': ", string + "TEST")
