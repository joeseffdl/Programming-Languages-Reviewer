dict = {"Name": "Culette", "Age": 5, "Class": "Dog"}

# len() function returns the number of items in the dictionary
print("dict: ", dict)
print("len(dict): ", len(dict)) # 3

# str() function returns the string version of the dictionary
print("dict: ", dict)
print("str(dict): ", str(dict)) # {'Name': 'Culette', 'Age': 5, 'Class': 'Dog'}

# type() function returns the type of the passed variable
print("dict: ", dict)
print("type(dict): ", type(dict)) # <class 'dict'>